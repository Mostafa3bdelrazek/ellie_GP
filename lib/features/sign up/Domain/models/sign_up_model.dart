class SignUpModel {
  final String middleName;
  final String lastName;
  final String type;
  final String email;
  final String password;
  final int age;
  final String id;

  SignUpModel({
    required this.middleName,
    required this.lastName,
    required this.type,
    required this.email,
    required this.password,
    required this.age,
    required this.id,
  });
}
